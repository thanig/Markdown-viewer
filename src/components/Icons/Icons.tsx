// Get icon component by file extension
export const getFileIcon = (fileName: string, isExpanded?: boolean): JSX.Element => {
  if (isExpanded !== undefined) {
    // It's a folder
    return isExpanded ? <FolderOpenIcon size={16} /> : <FolderIcon size={16} />;
  }

  const ext = fileName.split('.').pop()?.toLowerCase();

  switch (ext) {
    case 'md':
    case 'markdown':
      return <MarkdownIcon size={16} className="icon-markdown" />;
    case 'json':
      return <JsonIcon size={16} className="icon-json" />;
    case 'html':
    case 'htm':
      return <HtmlIcon size={16} className="icon-html" />;
    case 'js':
    case 'jsx':
      return <CodeIcon size={16} className="icon-javascript" />;
    case 'ts':
    case 'tsx':
      return <TypeScriptIcon size={16} className="icon-typescript" />;
    case 'css':
    case 'scss':
    case 'sass':
    case 'less':
      return <CssIcon size={16} className="icon-css" />;
    case 'py':
      return <PythonIcon size={16} className="icon-python" />;
    default:
      return <FileIcon size={16} />;
  }
};
